package HolaMundo.Calculadora;

// Clase del obeto calculadora
public class Calculadora {
  // método para sumar
  public int sumar(int a, int b) {
    return a + b;
  }

  // método para multiplicar
  public int multiplicar(int a, int b) {
    return a * b;
  }

  // método para restar
  public int restar(int a, int b) {
    return a - b;
  }
}
